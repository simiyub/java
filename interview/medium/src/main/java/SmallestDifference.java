/**
 * This function will take two arrays and return an array that contains
 * two numbers with a difference that is closest to zero.
 * The numbers in the array should be in the order of one from first array
 * then the number from the second array.
 * There can only be one pair of numbers with a difference closest to zero.
 * */
public interface SmallestDifference {
    int[] difference(int[] firstArray, int[] secondArray);
}
